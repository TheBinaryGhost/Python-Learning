# Here we will learn about variables in Python
# Variables are used to store information
# In Python, you can create a variable by simply assigning a value to it
a = 10
print(a)  # This will print the value of variable a
print(type(a))  # This will print the type of variable a, which is an integer

# You can also change the value of a variable
b = 20
print(b)  # This will print the value of variable b
print(type(b))  # This will print the type of variable b, which is an integer

# Variables can store different types of data
my_string = "Hello, World!" # This is a string
my_float = 3.14 # This is a float
my_list = [1, 2, 3, 4, 5] # This is a list
my_dict = {"name": "Alice", "age": 30} # This is a dictionary
my_tuple = (1, 2, 3)  # This is a tuple
print(my_string, my_float, my_list, my_dict, my_tuple)  # This will print the values of the variables
print(type(my_string), type(my_float), type(my_list), type(my_dict), type(my_tuple))  # This will print the types of the variables

