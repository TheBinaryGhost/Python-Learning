#Type conversion
#Type conversion is the process of converting one data type into another.
#In Python, you can perform type conversion using built-in functions.
#For example, you can convert an integer to a float using float() function.
a = 10
b = float(a)  # Convert integer to float
print(b)  # This will print 10.0

#You can also convert a float to an integer using int() function.
c = 3.14
d = int(c)  # Convert float to integer
print(d)  # This will print 3

#You can convert a string to an integer using int() function.
e = "5"
f = int(e)  # Convert string to integer
print(f)  # This will print 5

#You can convert an integer to a string using str() function.
g = 10
h = str(g)  # Convert integer to string
print(h)  # This will print "10"

# You can also convert a string to a float using float() function.
i = "3.14"
j = float(i)  # Convert string to float
print(j)  # This will print 3.14

